    private void getListSV(){
        con= getConnection();
        try {
            stm=con.createStatement();
            ResultSet rs= stm.executeQuery("select * from SVIEN where MaLop in(select MaLop from Lop where MaGiaoVien='"+DangNhapForm.magv+"')");
            while (rs.next()) {                
                String maSV= rs.getString("MaSinhVien");
                String maLop= rs.getString("MaLop");
                float diemDG= rs.getFloat("DiemDanhGia");
                float diemTichLuy= rs.getFloat("DiemTichLuy");
                String hoTen = rs.getString("HoTen");
                String quocTich = rs.getString("QuocTich");
                String xa = rs.getString("Xa");
                String huyen = rs.getString("Quan");
                String tinh = rs.getString("Tinh");
                String soCMND = rs.getString("SoCMND");
                String sdt = rs.getString("SoDienThoai");
                String maBHYT = rs.getString("MaBHYT");
                SinhVien s = new SinhVien(maSV,new Lop(maLop),diemTichLuy,diemDG,hoTen,quocTich,xa,
                huyen,tinh,soCMND,sdt,maBHYT);
                listSV.add(s);
            }
        } catch (Exception e) {
            System.out.println("Lỗi "+ e.getMessage());
        }
    }
    private void getListLop(){
        con= getConnection();
        try {
            stm=con.createStatement();
            ResultSet rs= stm.executeQuery("select * from LOP where MaGiaoVien='"+DangNhapForm.magv+"'");
            while (rs.next()) {  
                jComboBox1.addItem(rs.getString("TenLop"));
                String maLop = rs.getString("MaLop");
                String tenLop =rs.getString("TenLop");
                String maGV= rs.getString("MaGiaoVien");
                String maKhoa =rs.getString("MaKhoa");
                Lop l = new Lop(maLop,tenLop,new Khoa(maKhoa),new GiaoVien(maGV));
                listLop.add(l);
            }
        } catch (Exception e) {
            System.out.println("Lỗi "+e.getMessage());
        }
    }

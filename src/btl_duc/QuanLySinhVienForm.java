    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
       List<SinhVien> list = new ArrayList<>();
       List<SinhVien> listFind = tDataSinhVien.getList();
        try {
            if(jRadioButton3.isSelected()){
                if(jTextField1.getText().equals("")){
                    JOptionPane.showMessageDialog(this, "Vui lòng nhập mã sinh viên để tìm kiếm!","Chú Ý",JOptionPane.WARNING_MESSAGE);
                    return;  
                }
                String maSV = jTextField1.getText();
                for(SinhVien sinhVien:listFind){
                    if(maSV.equals(sinhVien.getMaSV())){
                        list.add(sinhVien);
                    }
                }
                loadTable(list);
                JOptionPane.showMessageDialog(this, "Có "+list.size()+" kết quả trùng khớp.");
                return;
            }
            if(jRadioButton4.isSelected()){
                if(jTextField2.getText().equals("")){
                    JOptionPane.showMessageDialog(this, "Vui lòng nhập họ tên để tìm kiếm!","Chú Ý",JOptionPane.WARNING_MESSAGE);
                    return;  
                }
                String hoTen = jTextField2.getText();
                for(SinhVien sinhVien:listFind){
                    if(hoTen.equals(sinhVien.getHoTen())){
                        list.add(sinhVien);
                    }
                }
                loadTable(list);
                JOptionPane.showMessageDialog(this, "Có "+list.size()+" kết quả trùng khớp.");
                return;
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi "+e.getMessage(),"Lỗi",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton8ActionPerformed


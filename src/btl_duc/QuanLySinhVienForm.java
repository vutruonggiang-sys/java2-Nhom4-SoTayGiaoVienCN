    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       
        try {
            String MaSV= jTextField1.getText();
            if(MaSV.equals("")){
                JOptionPane.showMessageDialog(this, "Vui lòng nhập mã sinh viên!","Chú ý",JOptionPane.WARNING_MESSAGE);
                return;
            }
            int k= JOptionPane.showConfirmDialog(this,"Bạn có chắc muốn xóa không?","Xác nhận xóa",JOptionPane.YES_NO_OPTION);
            if(k==JOptionPane.YES_OPTION){
                
                SinhVien s = new SinhVien(MaSV);
                if(listSV.contains(s)){
                    con=getConnection();
                    try {
                        stm=con.createStatement();
                        int r= stm.executeUpdate("delete from SVien where MaSinhVien='"+MaSV+"'");
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "Lỗi "+e.getMessage(),"Lỗi",JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                    listSV.remove(s);
                    loadTable(listSV);
                    JOptionPane.showMessageDialog(this, "Xóa thành công.");
                    jButton5ActionPerformed(evt);
                    return;
                }
                JOptionPane.showMessageDialog(this, "Không tồn tại sinh viên muốn xóa!","Chú ý",JOptionPane.WARNING_MESSAGE);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi "+e.getMessage(),"Lỗi",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
       jTable1.clearSelection();
       String tenLop = evt.getItem().toString();
       List<SinhVien> list = new ArrayList<>();
       if(tenLop.equalsIgnoreCase("Tất cả")){
           loadTable(listSV);
       }
       else{
           con= getConnection();
        try {
            list.clear();
            stm=con.createStatement();
            ResultSet rs= stm.executeQuery("select * from SVIEN inner join Lop on SVien.MaLop=Lop.MaLop where TenLop='"+tenLop+"'");
            while (rs.next()) {                
                String maSV= rs.getString("MaSinhVien");
                String maLop= rs.getString("MaLop");
                float diemDG= rs.getFloat("DiemDanhGia");
                float diemTichLuy= rs.getFloat("DiemTichLuy");
                String hoTen = rs.getString("HoTen");
                String quocTich = rs.getString("QuocTich");
                String xa = rs.getString("Xa");
                String huyen = rs.getString("Quan");
                String tinh = rs.getString("Tinh");
                String soCMND = rs.getString("SoCMND");
                String sdt = rs.getString("SoDienThoai");
                String maBHYT = rs.getString("MaBHYT");
                SinhVien s = new SinhVien(maSV,new Lop(maLop),diemTichLuy,diemDG,hoTen,quocTich,xa,
                huyen,tinh,soCMND,sdt,maBHYT);
                list.add(s);
            }
            loadTable(list);
        } catch (Exception e) {
            System.out.println("Lỗi "+ e.getMessage());
        }
       } 
    }//GEN-LAST:event_jComboBox1ItemStateChanged


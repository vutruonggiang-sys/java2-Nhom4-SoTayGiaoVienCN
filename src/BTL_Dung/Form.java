    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        Connect cn = new Connect();
        cn.getConnect();
        if(txtMaGV.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Lỗi không có mã giáo viên");
        }
        try {
            cn.update(txtMaGV.getText(), txtHoTen.getText(), txtQuocTich.getText(), txtXa.getText(), txtQuan.getText(), txtTinh.getText(), txtSoCMND.getText(), txtSDT.getText());
        } catch (Exception ex) {
            Logger.getLogger(Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        LoadData();
        try {
            cn.closeConnect();
        } catch (SQLException ex) {
            Logger.getLogger(Form.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


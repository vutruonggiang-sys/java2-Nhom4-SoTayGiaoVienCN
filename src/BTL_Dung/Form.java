    private void LoadData() {
        //Connect cn=new Connect();
        cn.getConnect();
        try {
            rs = cn.GetData("GiaoVien");
            jTable.removeAll();
            String[] tieuDe = {"MaGiaoVien", "HoTen", "QuocTich", "Xa", "Quan", "Tinh", "SoCMND", "SoDienThoai"};
            DefaultTableModel model = new DefaultTableModel(tieuDe, 0);
            while (rs.next()) {
                Vector vt = new Vector();
                vt.add(rs.getString("MaGiaoVien"));
                vt.add(rs.getString("HoTen"));
                vt.add(rs.getString("QuocTich"));
                vt.add(rs.getString("Xa"));
                vt.add(rs.getString("Quan"));
                vt.add(rs.getString("Tinh"));
                vt.add(rs.getString("SoCMND"));
                vt.add(rs.getString("SoDienThoai"));
                model.addRow(vt);
            }
            jTable.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(Form.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

